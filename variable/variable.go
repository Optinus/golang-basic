package variable

import "fmt"

func Variable() {
	// Deklarasi dan inisialisasi variabel menggunakan var keyword
	var variable1 string = "variable With the var keyword"

	// Pendekatan pendek untuk deklarasi dan inisialisasi variabel menggunakan := sign
	variable2 := "variable With the := sign"

	// Menampilkan nilai variabel ke layar
	fmt.Println(variable1)
	fmt.Println(variable2)
}
